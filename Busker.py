class World:
    def __init__(self, data):
        self.tiles = []
        self.tiles_collisions = []
        self.tile_die_or_win = []

        for index_row, row in enumerate(data):
            for index_title, tile_type in enumerate(row):
                if tile_type == 0:
                    air = pygame.Surface((tile, tile))
                    air.fill((0, 0, 189))
                    rect = air.get_rect()
                    rect.x = index_title * tile
                    rect.y = index_row * tile
                    self.tiles.append((air, rect))
                if tile_type == 1:
                    dirt = pygame.Surface((tile, tile))
                    dirt.fill((50, 0, 0))
                    rect = dirt.get_rect()
                    rect.x = index_title * tile
                    rect.y = index_row * tile
                    self.tiles.append((dirt, rect))
                    self.tiles_collisions.append((dirt, rect))
                if tile_type == 2:
                    lava = pygame.Surface((50, 20))
                    lava.fill("Red")
                    rect = lava.get_rect()
                    rect.x = index_title * tile
                    rect.y = index_row * tile + 30
                    self.tile_die_or_win.append(rect)
                    self.tiles.append((lava, rect))
                if tile_type == 3:
                    door = pygame.Surface((50, 100))
                    door.fill("brown")
                    rect = door.get_rect(midleft=(index_title * tile, index_row * tile))
                    self.tile_die_or_win.append(rect)
                    self.tiles.append((door, rect))

    def draw(self):
        for tile in self.tiles:
            WINDOW.blit(tile[0], tile[1])


class Player:
    def __init__(self):
        self.body = pygame.Surface((tile, 2 * tile))
        self.body.fill((103, 68, 189))
        self.rect = self.body.get_rect()
        self.rect.x = 0
        self.rect.y = 850
        self.ground = True
        self.dy = 0

    def move_and_collisions(self):
        dx = 0
        self.dy += 5
        keys = pygame.key.get_pressed()
        if keys[pygame.K_d]:
            dx += 10
        if keys[pygame.K_a]:
            dx -= 10
        if keys[pygame.K_SPACE] and self.ground is True:
            self.dy -= 40
            self.ground = False

        for collision in world.tiles_collisions:
            if collision[1].colliderect(self.rect.x, self.rect.y + self.dy, 50, 100):
                if self.dy >= 0:
                    self.dy = 0
                    self.ground = True
                elif self.dy < 0:
                    self.dy = 0
            if collision[1].colliderect(self.rect.x + dx, self.rect.y, 50, 100):
                dx = 0
        self.rect.x += dx
        self.rect.y += self.dy
        for collision in world.tile_die_or_win:
            if collision.colliderect(self.rect):
                global alive
                alive = False

    def draw(self):
        WINDOW.blit(self.body, self.rect)


